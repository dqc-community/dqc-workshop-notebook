@app.cell
def load_metadata(DATASET_DIR):
    """
    Reconstruct the metadata DataFrame from the on-disk QASM files.

    Returns a DataFrame with columns: n, hash, depth, duration_ns.
    """
    # The in-memory records from transpile-all.py are not persisted,
    # so we rebuild them by re-reading every .qasm file on disk.
    records = []
    for subdir in sorted(DATASET_DIR.iterdir()):
        if not subdir.is_dir() or not subdir.name.startswith("n_"):
            continue
        n = int(subdir.name.split("_")[1])
        for fp in sorted(subdir.glob("*.qasm")):
            # Skip the lock file used during generation.
            if fp.name.startswith("_"):
                continue
            # Parse HASH-COUNTER.qasm
            parts = fp.stem.rsplit("-", 1)
            if len(parts) != 2:
                continue
            circuit_hash, counter = parts[0], int(parts[1])
            records.append(
                dict(n=n, hash=circuit_hash, counter=counter, path=str(fp))
            )

    return pd.DataFrame(records)
