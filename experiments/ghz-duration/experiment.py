@app.cell
def _(enrich_metadata):
    mo.md(r"""
    ## Summary statistics per ``n``

    For each qubit count we compute the mean, median, std, min, and max
    of the estimated circuit duration.
    """)


@app.cell
def _(enrich_metadata):
    summary = (
        enrich_metadata.groupby("n")["duration_ns"]
        .agg(
            count="count",
            mean="mean",
            median="median",
            std="std",
            min="min",
            max="max",
        )
        .reset_index()
    )
    summary["mean_us"]  = summary["mean"]  / 1e3
    summary["median_us"]= summary["median"]/ 1e3
    summary["std_us"]   = summary["std"]  / 1e3
    summary
    return (summary,)


@app.cell
def _(enrich_metadata):
    mo.md(r"""
    ## Hash-collision rate

    The ratio of total files to unique hashes tells us how often the
    transpiler produced a result identical to a prior run.
    """)


@app.cell
def _(enrich_metadata):
    n_total  = len(enrich_metadata)
    n_unique = enrich_metadata["hash"].nunique()
    collision_rate = 1 - n_unique / n_total
    dict(n_total=n_total, n_unique=n_unique, collision_rate=f"{collision_rate:.1%}")


