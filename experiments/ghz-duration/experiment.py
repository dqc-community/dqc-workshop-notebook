# Load circuit objects & extract depth / duration
# ---------------------------------------------------------------------------

@app.cell
def enrich_metadata(_loaded_df):
    """
    Re-transpile each unique hash (without re-running the stochastic
    transpiler) to obtain deterministic metadata: depth and duration_ns.

    ``duration_ns`` is deterministic for a given QASM string + backend target,
    so one representative transpilation per unique hash is sufficient.
    """
    backend = qiskit_ibm_runtime.fake_provider.FakeSherbrooke()

    # Work on one row per unique hash to avoid redundant Qiskit calls.
    unique = (
        _loaded_df[["n", "hash"]]
        .drop_duplicates(subset="hash")
        .copy()
    )

    records = []
    for _, row in unique.iterrows():
        qasm_str = Path(
            _loaded_df[_loaded_df["hash"] == row["hash"]].iloc[0]["path"]
        ).read_text()
        try:
            circ = qiskit.QuantumCircuit.from_qasm(qasm_str)
        except Exception:
            # Malformed QASM — skip.
            continue
        depth       = circ.depth()
        duration_ns = circ.estimate_duration(target=backend.target)
        records.append(dict(hash=row["hash"], depth=depth, duration_ns=duration_ns))

    meta = pd.DataFrame(records)
    enriched = _loaded_df.merge(meta, on="hash", how="left")
    return enriched


